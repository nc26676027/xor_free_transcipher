// CombineBin
func CombineBin(var1, var2 []*BitSet) []*BitSet {
	var result []*BitSet
	for _, v1 := range var1 {
		for _, v2 := range var2 {
			result = append(result, Xor(v1, v2) )
		}
	}
	return result
}

// LayeredCombineBin
func LayeredCombineBin(variables []*BitSet) ([]*BitSet, error) {
	if len(variables)%2 != 0 {
		return nil, errors.New("the input must be even")
	}

	if len(variables) == 2 {
		result := []*BitSet{variables[0], variables[1], Xor(variables[0], variables[1]) }
		return result, nil
	}

	mid := len(variables) / 2
	left, err := LayeredCombineBin(variables[:mid])
	if err != nil {
		return nil, err
	}
	right, err := LayeredCombineBin(variables[mid:])
	if err != nil {
		return nil, err
	}

	combined := CombineBin(left, right)
	result := append(combined, left...)
	result = append(result, right...)

	return result, nil
}

// Combine 
func Combine(eval *bootstrapping.Evaluator, var1, var2 []*rlwe.Ciphertext ) []*rlwe.Ciphertext {
	result := make([]*rlwe.Ciphertext, 0)
	for i:=0;i<len(var1);i++ {
		for j:=0;j<len(var2);j++ {
			tmp, err := eval.MulRelinNew(var1[i], var2[j])
			if err != nil {
				panic(err)
			}
			eval.Rescale(tmp, tmp)
			result = append(result, tmp)
		}
	}
	return result
}

// LayeredCombine
func LayeredCombine(eval *bootstrapping.Evaluator, variables []*rlwe.Ciphertext) ([]*rlwe.Ciphertext, error) {
	if len(variables)%2 != 0 {
		return nil, errors.New("the input must be even")
	}

	if len(variables) == 2 {
		result := make([]*rlwe.Ciphertext, 0)
		result = append(result, variables[0], variables[1])
		tmp, err := eval.MulRelinNew(variables[0], variables[1])
		if err != nil {
			panic(err)
		}
		eval.Rescale(tmp, tmp)
		result = append(result, tmp)
		return result, nil
	}

	mid := len(variables) / 2
	left, err := LayeredCombine(eval, variables[:mid])
	if err != nil {
		return nil, err
	}
	right, err := LayeredCombine(eval, variables[mid:])
	if err != nil {
		return nil, err
	}
	combined := Combine( eval, left, right)
	result := append(combined, left...)
	result = append(result, right...)

	return result, nil
}

